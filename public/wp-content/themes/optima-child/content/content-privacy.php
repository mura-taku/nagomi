        ・患者さんの診療等のため、外部の医師等の意見・助言を求める場合<br>
        ・検体検査業務その他の業務委託<br>
        ・ご家族等への病状説明<br>
        ・医療事務の委託<br>
        ・審査支払機関へのレセプトの提供<br>
        ・審査支払機関または保険者からの照会への回答<br>
        ・事業者等から委託を受けた健康診断にかかわる、事業者等への結果通知<br>
        ・医師賠償責任保険等にかかわる、医療に関する専門の団体や保険会社等への相談または届出等<br>
        ・その他、患者さんへの医療事務に関する利用
      </p>
