        ・外部監査機関への情報提供<br>
        ・外来患者さんへの呼び出しについて<br>
        ・会計時の呼び出し掲示について<br>
        <br>
        前述のうち、他の医療機関等への情報提供について同意しがたい事項がある場合には、その旨をお申し出ください。<br>
        お申し出のないものについては、同意していただけたものとして取扱わさせていただきます。
      </p>
    </div>
